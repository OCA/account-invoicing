from openerp import api, models


class SaleOrder(models.Model):

    _inherit = 'sale.order'

    @api.multi
    def _prepare_invoice(self, context=None):

        invoice_vals = super(SaleOrder, self)._prepare_invoice()
        invoice_vals.update({
            'address_shipping_id': self.partner_shipping_id.id, })
        return invoice_vals
